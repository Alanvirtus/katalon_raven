class taps {

	@Keyword
	def tapUIElement(TestObject to, String elementName, int timeout){
		try {
			KeywordUtil.logInfo("Buscando el elemento: " + elementName)

			'Esperar a que el objeto exista'
			Mobile.waitForElementPresent(to, timeout, FailureHandling.STOP_ON_FAILURE)

			Mobile.tap(to, 1, FailureHandling.STOP_ON_FAILURE)

			KeywordUtil.markPassed("Tap Success en el elemento: " + elementName)
		} catch (Exception e) {
			KeywordUtil.markFailedAndStop("Test Fail. Error al hacer Tap en el elemento: " + elementName + ". Exception: " + e.getMessage())
		}
	}
}